function Right_Rectangle(f,x_0,x_n,n)
    s = 0.0
    h = (x_n - x_0)/n
    for i in (x_0+h):h:x_n
        s+= f(i)*h
    end
    return s
end

function Left_Rectangle(f,x_0,x_n,n)
    s = 0.0
    h = (x_n - x_0)/n
    for i in x_0:h:x_n - h
        s+=f(i)*h
    end
    return s
end

function Central_Rectangle(f,x_0,x_n,n)
    s = 0.0
    h = (x_n - x_0)/n
    for i in x_0+h/2:h:x_n - h/2
        s+=f(i)*h
    end
    return s
end

function Trapezoids(f,x_0,x_n,n)
    s = 0.0
    h = (x_n - x_0)/n
    k = (f(x_n)+f(x_0))/2 * h
    for i in x_0+h:h:x_n - h
        s+= f(i)*h
    end
    return s + k
end

function Simpson(f,x_0,x_n,n)
    if n%2 != 0
        return error("Метод Симпсона не поддерживает нечетное число шагов!")
    end
    h = (x_n - x_0)/n
    a = [f(x_0 + i*h) for i in 0:n] # Лучше так для эффективной работы с плавающей арифметикой
    s = sum(j%2 == 0 ? a[j]*4 : a[j]*2 for j in 2:length(a)-1)
    return h/3 * ( s + a[end]+ a[1])
end

function Adaptive_Simpson(f,x_0,x_n,epsilon; depth = 0, max_depth = 50)
    h = x_n - x_0
    m = (x_0 + x_n)/2
    S = h/6 * (f(x_0) + 4*f((x_0 + x_n)/2) + f(x_n))
    S_left = h/12 * (f(x_0) + 4*f((x_0 + m)/2) + f(m))
    S_right = h/12 * (f(m) + 4*f((x_n + m)/2) + f(x_n))
    E = abs(S_left + S_right - S)/15
    if E < epsilon || depth >= max_depth
        return S_left + S_right
    else
        left = Adaptive_Simpson(f,x_0,m,epsilon/2;depth=depth+1,max_depth=max_depth)
        right = Adaptive_Simpson(f,m,x_n,epsilon/2;depth=depth+1,max_depth=max_depth)
        return left + right
    end
end

function Adaptive_Gaus_Kronrod(f,x_0,x_n,epsilon;depth = 0, max_depth = 50)
m = (x_n + x_0)/2
G15 = (x_n - x_0)/2* sum(weight_G[i]*f((x_n - x_0)/2 * node_GS[i]+(x_0 + x_n)/2) for i in 1:15)
K21 = (x_n - x_0)/2 * sum(weight_Kr[i]*f((x_n - x_0)/2 * node_Kr[i]+(x_0 + x_n)/2) for i in 1:21)
E = abs(K21 - G15)
if E < epsilon || depth >= max_depth
    return K21
else
    left = Adaptive_Gaus_Kronrod(f,x_0,m,epsilon/2;depth=depth+1,max_depth=max_depth)
    right = Adaptive_Gaus_Kronrod(f,m,x_n,epsilon/2;depth=depth+1,max_depth=max_depth)
    return left + right
end
end

